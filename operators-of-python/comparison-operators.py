# Takes input as a value(number or string) and gives output as boolean(true/false)
# <, >, ==(equal to), !=(not equal), <=, >=
a = 3
b = 4
c = a > b
d = a < b
e = a == b
f = a != b
g = a <= b # checks the both conditions, if any condition is true then it returns true
h = a >= b
print(c)
print(d)
print(e)
print(f)
print(g)
print(h)